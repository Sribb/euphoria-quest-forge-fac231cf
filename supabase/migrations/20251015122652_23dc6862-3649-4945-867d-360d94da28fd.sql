## The Three Essential Statements

### 1. Income Statement (Profit & Loss)
Shows revenue, expenses, and profit over a period:
- **Revenue (Top Line)**: Total sales. Growing revenue is good, but check the quality.
- **Gross Profit**: Revenue minus cost of goods sold. High margins suggest competitive advantage.
- **Operating Income**: Profit from core business operations.
- **Net Income (Bottom Line)**: What''s left after all expenses, interest, and taxes.

### 2. Balance Sheet
A snapshot of what the company owns and owes:
- **Assets**: Cash, inventory, property, equipment. Current assets = liquid within a year.
- **Liabilities**: Debts and obligations. Current liabilities = due within a year.
- **Equity**: Assets minus liabilities. This is shareholder value.

### 3. Cash Flow Statement
Tracks actual cash moving in and out:
- **Operating Cash Flow**: Cash from normal business operations. Should be positive and growing.
- **Investing Cash Flow**: Spending on equipment, acquisitions. Negative isn''t always bad.
- **Financing Cash Flow**: Borrowing, repaying debt, dividends.

## Key Ratios to Calculate

**Profitability:**
- Net Profit Margin = Net Income ÷ Revenue (higher is better)
- Return on Equity (ROE) = Net Income ÷ Shareholder Equity (measures efficiency)

**Liquidity:**
- Current Ratio = Current Assets ÷ Current Liabilities (above 1.5 is healthy)
- Quick Ratio = (Current Assets - Inventory) ÷ Current Liabilities (stricter test)

**Leverage:**
- Debt-to-Equity = Total Debt ÷ Shareholder Equity (lower is safer)

**Valuation:**
- P/E Ratio = Price per Share ÷ Earnings per Share (compare to industry average)

## Red Flags to Watch For

- Declining revenue while competitors grow
- Profit margins shrinking quarter after quarter
- Operating cash flow is negative or much lower than net income
- Debt-to-equity above 2 (except for banks/utilities)
- Frequent changes in accounting methods

