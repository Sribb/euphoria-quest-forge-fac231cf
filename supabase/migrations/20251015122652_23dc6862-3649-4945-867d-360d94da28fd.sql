## Dividend Metrics

**Dividend Yield** = Annual Dividend ÷ Stock Price
- Example: $4 annual dividend, $100 stock = 4% yield
- Higher isn''t always better—extremely high yields may signal trouble

**Payout Ratio** = Dividends ÷ Earnings
- Below 60% is generally sustainable
- Above 80% may indicate the dividend is at risk

**Dividend Growth Rate**
- Track how much the dividend has increased annually
- Consistent 5-10% annual growth is excellent

