import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Pluto, a friendly financial literacy assistant. You do not offer financial advice, recommendations, or predictions. You help users understand their data and learn about financial concepts. Always remind users to consult a licensed financial advisor for personalized guidance.

STRICT RULES:
- NEVER recommend buying, selling, or holding any specific asset
- NEVER predict market movements or future prices
- NEVER suggest portfolio changes or rebalancing strategies
- NEVER imply that any investment is "safe," "risky," or "a good idea"
- DO answer educational questions about how markets, assets, or trading concepts work
- DO summarize factual, objective data the user already has access to
- DO redirect financial decision questions with: "I'm not a financial advisor — for personalized advice, consider consulting a licensed professional. But here's what the data shows…"
- DO encourage users to do their own research (DYOR) and make informed decisions independently
- Keep responses concise (2-4 paragraphs max)
- Be warm, clear, and on-brand`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY not configured");

    const systemMessages = [
      { role: "system", content: SYSTEM_PROMPT },
      ...(context ? [{ role: "system", content: `User context: ${context}` }] : []),
    ];

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [...systemMessages, ...messages],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited. Please try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Credits exhausted. Please add funds." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      throw new Error("AI gateway error");
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content || "I couldn't process that right now.";

    return new Response(JSON.stringify({ content }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("ask-pluto error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
